	DEC	A
	CP	14
	JR	C,ERROR1
	INC	A
	LD	DE,ER15
	PUSH	AF
	CALL	MSG
	LD	A,'$'	;１６進数ヘッダ
	CALL	PRINT
	POP	AF
	CALL	PRTHX
	JR	ERROR2
ERROR1:	LD	HL,MESTBL
	ADD	A,A
	LD	E,A
	LD	D,0
	ADD	HL,DE
	LD	E,(HL)
	INC	HL
	LD	D,(HL)
	CALL	MSG
ERROR2:	CALL	BELL
	CALL	NL

	RET

PRTHX:
	PUSH	AF
	RLCA
	RLCA
	RLCA
	RLCA		;上位
	CALL	ASC
	CALL	_PRINT
	POP	AF	;下位
	CALL	ASC
	CALL	_PRINT
	OR	A	;ＣＹ←０
	RET

