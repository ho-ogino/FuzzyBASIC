	RET

_GETL:
GETL:
	PUSH	BC
	PUSH	HL
	PUSH	DE

	;----------------
	;Breakを抑制
	LD	HL,ZGETLBRK
	LD	(00001H),HL
	LD	(SPBK),SP
	;----------------

	; INSFLGクリア
	XOR	A
	LD	(INSFLG),A

	CALL	FGETL
	JR	NC,ZGETL_CR

	CALL	_KYBFC

	; 1文字ループ（BDOS $0A の代替）
ZGETL_LOOP:
	CALL	_FLGET
	CP	$0D
	JR	Z,ZGETL_CR
	CP	$03
	JR	Z,ZGETL_BRK
	CP	$1B
	JR	Z,ZGETL_BRK
	CP	$09
	JR	Z,ZGETL_TAB
	CP	$11		; Ctrl+Q = INSERT toggle
	JR	Z,ZGETL_INS
	CP	$7F		; DEL
	JR	Z,ZGETL_DEL
	; 通常文字: INSERTモード処理 → PRINT
	CALL	GTINS_LOCAL
	CALL	PRINT
	JR	ZGETL_LOOP

ZGETL_TAB:
	; TABをスペース展開
	LD	A,(XYADR)
	AND	7
	LD	B,A
	LD	A,8
	SUB	B
	LD	B,A
.ZGTAB_LP
	LD	A,$20
	CALL	GTINS_LOCAL
	CALL	PRINT
	DJNZ	.ZGTAB_LP
	JR	ZGETL_LOOP

ZGETL_INS:
	; INSERTモード トグル
	LD	HL,INSFLG
	LD	A,(HL)
	CPL
	LD	(HL),A
	JR	ZGETL_LOOP

ZGETL_DEL:
	; DELキー: カーソル位置の文字を削除
	; 簡易実装: カーソル〜論理行末尾を左シフト、末尾にスペース
	PUSH	BC
	PUSH	DE
	PUSH	HL
	; カーソルofs
	LD	A,(CSRNY)
	LD	H,A
	LD	A,(XYADR)
	LD	L,A
	CALL	LOC1_LOCAL	; HL = カーソルofs
	PUSH	HL
	; 論理行末尾ofs
	CALL	GETLNL_LOCAL	; A = 末尾+1行番号
	LD	H,A
	LD	L,0
	CALL	LOC1_LOCAL	; HL = 末尾+1行先頭
	DEC	HL		; HL = 末尾ofs
	EX	DE,HL		; DE = 末尾ofs
	POP	HL		; HL = カーソルofs（=開始）
	; VRAM左シフト
	CALL	VSHIFT_LEFT
	; 末尾にスペース
	EX	DE,HL		; HL = 末尾ofs
	LD	A,$20
	CALL	PUTCHR_LOCAL
	POP	HL
	POP	DE
	POP	BC
	JR	ZGETL_LOOP

ZGETL_BRK:
	; ブレーク
	JR	ZGETL_BRKX

	;----------------
	;Break押されて飛んできたのでSPを戻す
ZGETLBRK:
	LD	HL,(SPBK)
	LD	SP,HL
	;----------------
ZGETL_BRKX:
	POP	DE
	PUSH	DE
	LD	A,$1B
	LD	(DE),A
	INC	DE
	XOR	A
	LD	(DE),A
	JR	ZGETL_EXIT

ZGETL_CR:
	; CR: VRAMから論理行を読み取り
	POP	DE
	PUSH	DE
	CALL	INPUTL_LOCAL	; DE = バッファに論理行内容

ZGETL_EXIT:
	; INSFLGクリア
	XOR	A
	LD	(INSFLG),A

	POP	DE
	POP	HL
	POP	BC
	LD	A,$0D
	JP	PRINT

;
FGETL:
	CALL	FGETL1
	RET	C
;
	LD	HL,$0D00
	LD	(KBFAD1),HL
	RET
;
FGETL1:
	CALL	_BRKEY
	SCF
	RET	Z
	CALL	INKBF
;
	CP	$0D
	RET	Z
	CP	$0A
	JR	Z,FGETL1
;
	OR	A
	SCF
	RET	Z
	CP	$1A
	SCF
	RET	Z
;
	CP	$09
	JR	NZ,FGETL2
	LD	A,$20
FGETL2:
	CALL	PRINT
	JR	FGETL1

;
INKBF:
	PUSH	HL
	LD	HL,(SUBPS)
	LD	A,H
	XOR	L
	JR	Z,INKBF1
	LD	A,H
	INC	A
	LD	(SUBPS+1),A
	LD	L,A
	LD	H,0
	LD	BC,SUBBF
	ADD	HL,BC
	LD	A,(HL)
	SCF
INKBF1:
	POP	HL
	RET
;
ASC:
	AND	$0F
	OR	$30
	CP	$3A
	RET	C
	ADD	A,7
	RET

