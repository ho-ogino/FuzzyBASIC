_MPRNT:
MPRNT:
	EX	(SP),HL
MPRNT1:
	LD	A,(HL)
	INC	HL
	OR	A
	CALL	NZ,_PRINT
	OR	A
	JR	NZ,MPRNT1
	EX	(SP),HL
	RET
_NL:
NL:
	PUSH	AF
	LD	A,(XYADR)
	OR	A
	JR	Z,NLX
	CALL	ZPRINT
	DB	$0D,$0A,0
	CALL	PCLR
	CALL	NEWLINE		; CSRNY++, LINTTB更新
NLX:
	POP	AF
	RET

LTNL:
	LD	A,$0D
	JR	LTNL1
PRINTS:
	LD	A,$20
LTNL1:
