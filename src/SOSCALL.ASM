	CALL	_PRINT
	POP	AF
	RET

_CSR:
CSR:
	; H=Y行, L=X列
	LD	A,(XYADR)
	LD	L,A
	LD	A,(CSRNY)
	LD	H,A
	RET
_SCRN:
SCRN:
	; 入力: H=行, L=列  出力: A=文字 (CY=0)
	PUSH	BC
	PUSH	DE
	PUSH	HL
	CALL	LOC1_LOCAL
	CALL	GETCHR_LOCAL
	CALL	VRAM2ASCII
	POP	HL
	POP	DE
	POP	BC
	OR	A		; CY=0 (正常終了)
	RET

_LOC:
LOC:
	; 入力: H=Y行, L=X列
	LD	A,L
	LD	(XYADR),A
	LD	A,H
	LD	(CSRNY),A
	; ESC Yシーケンス出力
	LD	A,H
	ADD	A,$20
	LD	(LOCY),A
	LD	A,L
	ADD	A,$20
	LD	(LOCX),A
	CALL	ZPRINT
	DB	$1B,"Y"
LOCY:	DB	$20
LOCX:	DB	$20
	DB	0
	AND	A		;CF=0
	RET

PAUSE:
	CALL	_BRKEY
	JR	Z,PAUSE2
	CP	$20
	JR	NZ,PAUSE1
;
NULL:
	CALL	_GETKY
	OR	A
	JR	NZ,NULL
	CALL	_INKEY
	CALL	BRKEY1
	JR	Z,PAUSE2
;
PAUSE1:
	EX	(SP),HL
	INC	HL
	INC	HL
	EX	(SP),HL
	RET
;
PAUSE2:
	EX	(SP),HL
	LD	A,(HL)
	INC	HL
	LD	H,(HL)
	LD	L,A
	EX	(SP),HL
	RET

_BRKEY:
BRKEY:
	CALL	_GETKY
BRKEY1:
	CP	$1B
	RET	Z
	CP	3
	RET	Z
	; Break以外の文字はプッシュバック
	OR	A
	JR	Z,.BRK_NOKEY
	LD	(KEYPB),A
	OR	A		; NZ（Breakでない）, A=キー文字を維持
	RET
.BRK_NOKEY
	OR	$FF		; NZ（Breakでない）
	RET
HEX:
	CALL	_CAP
	SUB	$30
	RET	C
	CP	$0A
	CCF
	RET	NC
	CP	$11
	RET	C
	SUB	7
	CP	$10
	CCF
	RET
_CAP:
CAP:
