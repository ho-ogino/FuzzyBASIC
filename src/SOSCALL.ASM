_PRINT:
PRINT:
	PUSH	BC
	PUSH	DE
	PUSH	HL
	PUSH	AF
	LD	E,A
	CP	$20
	JR	C,CTRL
PRINT1:
	LD	HL,(XYADR)
	LD	H,0
	LD	A,(AT_WIDTH)
	LD	B,A
	LD	A,L
	SUB	B
	JR	C,PR1
	; 折り返し発生
	LD	L,0
	CALL	WRAP_LINE
PR1:
	INC	L
	LD	(XYADR),HL
	DEC	L
	LD	BC,PRBF
	ADD	HL,BC
	LD	(HL),E
;
PRINT2:
	LD	C,$06		; INPOUT ($FF is special: console input)
	LD	A,E
	INC	A
	JR	NZ,.PRINT_BDOS
	LD	C,_CONOUT
.PRINT_BDOS
	CALL	SYSTEM
PRINT3:
	POP	AF
	POP	HL
	POP	DE
	POP	BC
	AND	A
	RET

PCLR:
	XOR	A
	LD	(XYADR),A
	RET
;
CTRL:
	CP	$0D
	JR	Z,CTRL_M
	CP	7
	JR	Z,PRINT2
	CP	$0C
	JR	Z,CTRL_L
	CP	$1A
	JR	Z,CTRL_Z
	CP	$0B
	JR	Z,CTRL_K
	CP	$08
	JR	Z,CTRL_H
	CP	$0A
	JP	Z,CTRL_J
	CP	9
	JP	Z,CTRL_I
	CP	$1E
	JP	Z,CTRL_CUR_U
	CP	$1F
	JP	Z,CTRL_CUR_D
	CP	$1C
	JP	Z,CTRL_CUR_R
	CP	$1D
	JP	Z,CTRL_CUR_L
	JR	PRINT3
CTRL_M:
	; CR: X=0, Y++, LINTTB[Y]=$00, 出力
	CALL	PCLR
	CALL	NEWLINE
	LD	C,$06		;INPOUT
	CALL	SYSTEM
	LD	E,$0A		;CRLF
	JR	PRINT2
CTRL_L:
	; CLS: Y=0, CLR_LINTTB
	CALL	PCLR
	CALL	CLR_LINTTB
	XOR	A
	LD	(CSRNY),A
	CALL	ZPRINT
CTLX1:	DB	$1B,"E",0
	JR	PRINT3
CTRL_Z:
	CALL	ZPRINT
CTZX1:	DB	$1B,"J",0
	JR	PRINT3
CTRL_K:
	; HOME: X=0, Y=0
	CALL	PCLR
	XOR	A
	LD	(CSRNY),A
	CALL	ZPRINT
CTKX1:	DB	$1B,"H",0
	JR	PRINT3
CTRL_H:
	; BS: 破壊的バックスペース（S-OS準拠）
	; 論理行先頭なら何もしない、それ以外は左移動+DEL
	LD	A,(XYADR)
	OR	A
	JR	NZ,.CTRL_H_NOTHEAD
	; X==0: 論理行先頭チェック
	CALL	GETLHD_LOCAL	; A = 論理行先頭行
	LD	B,A
	LD	A,(CSRNY)
	CP	B
	JP	Z,PRINT3	; 論理行先頭 → 何もしない
	; X==0だが論理行先頭ではない → 前行末尾へ移動
	DEC	A
	LD	(CSRNY),A
	LD	A,(AT_WIDTH)
	DEC	A
	LD	(XYADR),A
	JR	.CTRL_H_DEL
.CTRL_H_NOTHEAD
	DEC	A
	LD	(XYADR),A
.CTRL_H_DEL
	; カーソル位置の文字を削除（VRAM左シフト+末尾にスペース）
	LD	A,(CSRNY)
	LD	H,A
	LD	A,(XYADR)
	LD	L,A
	CALL	LOC1_LOCAL	; HL = カーソルVRAMオフセット
	PUSH	HL
	CALL	GETLNL_LOCAL	; A = 論理行末尾+1行
	LD	H,A
	LD	L,0
	CALL	LOC1_LOCAL	; HL = 末尾+1行先頭
	DEC	HL		; HL = 論理行最終バイト
	EX	DE,HL		; DE = 末尾オフセット
	POP	HL		; HL = カーソルオフセット
	CALL	VSHIFT_LEFT	; VRAM左シフト
	EX	DE,HL		; HL = 末尾オフセット
	LD	A,$20
	CALL	PUTCHR_LOCAL	; 末尾にスペース
	; カーソル位置をESC Yで設定
	JP	OUTPUT_CURSOR_POS
CTRL_J:
	; LF: Y++, スクロール判定
	LD	A,(CSRNY)
	INC	A
	CP	LINE
	JR	C,.CTRL_J_NOSC
	CALL	ROLUP_LINTTB
	LD	A,LINE-1
.CTRL_J_NOSC
	LD	(CSRNY),A
	JP	PRINT2		; BDOS出力
CTRL_I:
	LD	A,(XYADR)
	AND	7
	LD	B,A
	LD	A,8
	SUB	B
	LD	B,A
CTRL_I1:
	LD	A,$20
	CALL	PRINT
	DJNZ	CTRL_I1
	JP	PRINT3

; カーソルキーハンドラ
CTRL_CUR_U:
	; カーソル上: Y-- (0で停止)
	LD	A,(CSRNY)
	OR	A
	JP	Z,PRINT3	; 移動しない場合は何も出力しない
	DEC	A
	LD	(CSRNY),A
	JP	OUTPUT_CURSOR_POS

CTRL_CUR_D:
	; カーソル下: Y++ (LINE-1で停止)
	LD	A,(CSRNY)
	INC	A
	CP	LINE
	JP	NC,PRINT3	; 移動しない場合は何も出力しない
	LD	(CSRNY),A
	JP	OUTPUT_CURSOR_POS

CTRL_CUR_R:
	; カーソル右: X++, WIDTH到達で次行へ
	LD	A,(XYADR)
	INC	A
	LD	B,A
	LD	A,(AT_WIDTH)
	CP	B
	JR	NZ,.CUR_R_SET
	; 折り返し
	LD	B,0
	LD	A,(CSRNY)
	INC	A
	CP	LINE
	JR	NC,.CUR_R_SET2
	LD	(CSRNY),A
.CUR_R_SET2
.CUR_R_SET
	LD	A,B
	LD	(XYADR),A
	JP	OUTPUT_CURSOR_POS

CTRL_CUR_L:
	; カーソル左: X--, 0で前行末尾へ
	LD	A,(XYADR)
	OR	A
	JR	NZ,.CUR_L_DEC
	; X==0: 前行末尾
	LD	A,(CSRNY)
	OR	A
	JP	Z,PRINT3	; 左上端 → 何もしない
	DEC	A
	LD	(CSRNY),A
	LD	A,(AT_WIDTH)
	DEC	A
	LD	(XYADR),A
	JP	OUTPUT_CURSOR_POS
.CUR_L_DEC
	DEC	A
	LD	(XYADR),A
	JP	OUTPUT_CURSOR_POS

; ESC Y <row+$20> <col+$20> でカーソル位置を直接設定
OUTPUT_CURSOR_POS:
	LD	E,$1B
	LD	C,$06
	CALL	SYSTEM
	LD	E,'Y'
	LD	C,$06
	CALL	SYSTEM
	LD	A,(CSRNY)
	ADD	A,$20
	LD	E,A
	LD	C,$06
	CALL	SYSTEM
	LD	A,(XYADR)
	ADD	A,$20
	LD	E,A
	JP	PRINT2

