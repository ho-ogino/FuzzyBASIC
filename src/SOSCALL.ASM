	; プッシュバックバッファから返す
	PUSH	HL
	LD	HL,KEYPB
	LD	(HL),0
	POP	HL
	AND	A		; NZ（文字あり）
	RET
.GK_BDOS
	PUSH	BC
	PUSH	DE
	PUSH	HL
	LD	E,$FF
	LD	C,$06		;INPOUT
	JR	GETKY1
