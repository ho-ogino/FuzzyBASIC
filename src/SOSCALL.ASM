; LINTTB管理サブルーチン群

; LINTTBを全クリア（全行を論理行開始に）
CLR_LINTTB:
	PUSH	AF
	PUSH	BC
	PUSH	DE
	PUSH	HL
	LD	HL,LINTTB
	LD	DE,LINTTB+1
	LD	BC,LINE-1
	XOR	A
	LD	(HL),A
	LDIR
	POP	HL
	POP	DE
	POP	BC
	POP	AF
	RET

; LINTTBを1行上にスクロール（先頭行を破棄、末尾に$00追加）
ROLUP_LINTTB:
	PUSH	BC
	PUSH	DE
	PUSH	HL
	LD	HL,LINTTB+1
	LD	DE,LINTTB
	LD	BC,LINE-1
	LDIR
	XOR	A
	LD	(DE),A		; 末尾行=$00（新論理行）
	POP	HL
	POP	DE
	POP	BC
	RET

; 折り返し処理: Y++, LINTTB[Y]=$FF, スクロール判定
; 破壊: AF
; 折り返し処理: Y++, LINTTB[Y]=$FF, スクロール判定
WRAP_LINE:
	LD	A,$FF
	JR	SETLINE
; NEWLINE処理: Y++, LINTTB[Y]=$00（新論理行）, スクロール判定
NEWLINE:
	XOR	A
; 共通: A=LINTTB値($FF=継続, $00=新行)
SETLINE:
	PUSH	DE
	PUSH	HL
	PUSH	AF		; マーカ退避
	LD	A,(CSRNY)
	INC	A
	CP	LINE
	JR	C,.SL_NOSCROLL
	CALL	ROLUP_LINTTB
	LD	A,LINE-1
.SL_NOSCROLL
	LD	(CSRNY),A
	LD	E,A
	LD	D,0
	LD	HL,LINTTB
	ADD	HL,DE
	POP	AF		; マーカ復元
	LD	(HL),A
	POP	HL
	POP	DE
	RET

; (H=行, L=列) → HL=VRAMオフセット
; HL = AT_WIDTH * H + L
; 破壊: なし
LOC1_LOCAL:
	PUSH	DE
	PUSH	BC
	LD	C,L		; C = col
	LD	B,8
	LD	E,H		; E = row
	LD	D,0
	LD	A,(AT_WIDTH)
	LD	H,A
	LD	L,D		; HL = WIDTH << 8
.LOC1_MUL
	ADD	HL,HL
	JR	NC,.LOC1_NOADD
	ADD	HL,DE
.LOC1_NOADD
	DJNZ	.LOC1_MUL
	LD	B,D		; B = 0
	ADD	HL,BC		; + col
	POP	BC
	POP	DE
	RET

; VRAMから1バイト読み取り
; 入力: HL=VRAMオフセット  出力: A=文字コード
; 破壊: なし（BCは保存）
GETCHR_LOCAL:
	PUSH	BC
	LD	A,H
	OR	$30		; テキストVRAM上位 $30xx
	LD	B,A
	LD	C,L
	IN	A,(C)
	POP	BC
	RET

; VRAMに1バイト書き込み
; 入力: HL=VRAMオフセット, A=文字コード
; 破壊: なし（BCは保存）
PUTCHR_LOCAL:
	PUSH	BC
	PUSH	AF
	LD	A,H
	OR	$30
	LD	B,A
	LD	C,L
	POP	AF
	OUT	(C),A
	POP	BC
	RET

; VRAM右シフト（1バイト分）
; 入力: DE=開始オフセット, HL=終了オフセット
; VRAM[start..end-1] → VRAM[start+1..end]
; 破壊: AF, HL
VSHIFT_RIGHT:
	; バイト数チェック
	PUSH	HL
	OR	A
	SBC	HL,DE
	LD	A,H
	OR	L
	POP	HL
	RET	Z		; 0バイトなら何もしない
	; HL = 終了オフセット, DE = 開始オフセット
	; 末尾から先頭へコピー
.VSR_LOOP
	PUSH	HL
	DEC	HL
	CALL	GETCHR_LOCAL	; A = VRAM[HL-1]
	POP	HL
	CALL	PUTCHR_LOCAL	; VRAM[HL] = A
	DEC	HL
	; HL == DE で終了
	LD	A,H
	CP	D
	JR	NZ,.VSR_LOOP
	LD	A,L
	CP	E
	JR	NZ,.VSR_LOOP
	RET

; VRAM左シフト（1バイト分）
; 入力: HL=開始オフセット, DE=終了オフセット
; VRAM[start+1..end] → VRAM[start..end-1]
; 破壊: AF, HL
VSHIFT_LEFT:
	; バイト数チェック
	PUSH	HL
	EX	DE,HL
	OR	A
	SBC	HL,DE		; HL = end - start
	EX	DE,HL		; DE = end - start
	POP	HL		; HL = start
	LD	A,D
	OR	E
	RET	Z		; 0バイトなら何もしない
	; HL = 開始オフセット, 開始から終了へコピー
	PUSH	DE		; 保存（カウンタとして使わない、終了判定に使う）
	; 実際の終了ofsを再計算
	POP	DE
	PUSH	HL		; 開始位置保存
	ADD	HL,DE		; HL = 開始+差分 = 終了ofs
	EX	DE,HL		; DE = 終了ofs
	POP	HL		; HL = 開始ofs
.VSL_LOOP
	PUSH	HL
	INC	HL
	CALL	GETCHR_LOCAL	; A = VRAM[HL+1]
	POP	HL
	CALL	PUTCHR_LOCAL	; VRAM[HL] = A
	INC	HL
	LD	A,H
	CP	D
	JR	NZ,.VSL_LOOP
	LD	A,L
	CP	E
	JR	NZ,.VSL_LOOP
	RET

; VRAMを指定文字で埋める
; 入力: HL=開始オフセット, BC=バイト数, A=文字
; 破壊: BC, HL
FILVRM_LOCAL:
	PUSH	DE
	LD	D,A		; D = 文字
.FIL_LOOP
	LD	A,B
	OR	C
	JR	Z,.FIL_DONE
	LD	A,D
	CALL	PUTCHR_LOCAL
	INC	HL
	DEC	BC
	JR	.FIL_LOOP
.FIL_DONE
	POP	DE
	RET

; VRAM間ブロック転送
; 入力: HL=転送元, DE=転送先, BC=バイト数
; 方向自動判定（HL<DE: 逆方向、HL>=DE: 順方向）
; 破壊: AF, BC, DE, HL
VBTRNS_LOCAL:
	LD	A,B
	OR	C
	RET	Z
	PUSH	HL
	OR	A
	SBC	HL,DE
	POP	HL
	JR	NC,.VBT_FORWARD
	; 逆方向: 末尾から
	PUSH	BC
	ADD	HL,BC
	DEC	HL		; HL = 転送元末尾
	EX	DE,HL
	ADD	HL,BC
	DEC	HL		; HL = 転送先末尾
	EX	DE,HL		; HL=転送元末尾, DE=転送先末尾
	POP	BC
.VBT_REV
	CALL	GETCHR_LOCAL
	EX	DE,HL
	CALL	PUTCHR_LOCAL
	EX	DE,HL
	DEC	HL
	DEC	DE
	DEC	BC
	LD	A,B
	OR	C
	JR	NZ,.VBT_REV
	RET
.VBT_FORWARD
	CALL	GETCHR_LOCAL
	EX	DE,HL
	CALL	PUTCHR_LOCAL
	EX	DE,HL
	INC	HL
	INC	DE
	DEC	BC
	LD	A,B
	OR	C
	JR	NZ,.VBT_FORWARD
	RET

; CSRNYからLINTTBを逆走査して論理行先頭行番号を返す
; 出力: A = 論理行先頭行番号
; 破壊: DE, HL
GETLHD_LOCAL:
	LD	A,(CSRNY)
.GLHD_LOOP
	OR	A
	RET	Z		; 行0は必ず先頭
	LD	(.GLHD_ROW),A
	LD	E,A
	LD	D,0
	LD	HL,LINTTB
	ADD	HL,DE
	LD	A,(HL)
	OR	A
	LD	A,(.GLHD_ROW)	; LD A,(nn) はフラグ不変
	RET	Z		; $00 = この行が論理行開始
	DEC	A
	JR	.GLHD_LOOP
.GLHD_ROW
	DB	0

; CSRNYからLINTTBを順走査して論理行末尾+1行番号を返す
; 出力: A = 末尾+1行番号（最大LINE）
; 破壊: DE, HL
GETLNL_LOCAL:
	LD	A,(CSRNY)
	INC	A
.GLNL_LOOP
	CP	LINE
	RET	NC		; 画面外 → LINE を返す
	LD	(.GLNL_ROW),A
	LD	E,A
	LD	D,0
	LD	HL,LINTTB
	ADD	HL,DE
	LD	A,(HL)
	OR	A
	LD	A,(.GLNL_ROW)	; フラグ不変
	RET	Z		; $00 = 次の論理行開始
	INC	A
	JR	.GLNL_LOOP
.GLNL_ROW
	DB	0

; VRAM→バッファ読み取り（論理行全体）
; 入力: DE = 出力バッファ先頭
; 出力: バッファにVRAMの論理行内容をコピー（末尾$00）
; 破壊: AF, BC, DE, HL
INPUTL_LOCAL:
	PUSH	IX
	; バッファ先頭アドレスを保存（トリム用）
	LD	(.INL_BUFST),DE
	PUSH	DE
	POP	IX		; IX = バッファ先頭
	; 論理行先頭行取得
	CALL	GETLHD_LOCAL	; A = 先頭行番号
	LD	(.INL_CURROW),A
	; 論理行末尾+1行取得
	CALL	GETLNL_LOCAL	; A = 末尾+1行番号
	LD	(.INL_ENDROW),A

	LD	A,(AT_WIDTH)
	LD	(.INL_WIDTH),A
	; 128バイト上限カウンタ
	LD	B,127		; 最大127文字（+終端$00）

	LD	A,(.INL_CURROW)
.INL_ROWLOOP
	PUSH	AF		; 現在行番号
	; この行のVRAMオフセット算出
	LD	H,A
	LD	L,0
	CALL	LOC1_LOCAL	; HL = 行先頭VRAMオフセット

	LD	A,(.INL_WIDTH)
	LD	C,A		; C = 列カウンタ
.INL_COLLOOP
	CALL	GETCHR_LOCAL
	; 文字コード変換（VRAM→ASCII）
	CALL	VRAM2ASCII
	LD	(IX+0),A
	INC	IX
	INC	HL
	DEC	B
	JR	Z,.INL_FULL	; バッファ満杯
	DEC	C
	JR	NZ,.INL_COLLOOP

	POP	AF		; 行番号
	INC	A
	CP	0		; placeholder
.INL_ENDROW	EQU	$-1
	JR	C,.INL_ROWLOOP
	JR	.INL_TRIM	; 正常終了 — スタックは整合済み

.INL_FULL
	POP	AF		; バッファ満杯時のみ: 行番号のPUSH AFを解消
.INL_TRIM
	; 末尾トリム: $20以下を除去（バッファ先頭を下回らないように）
	LD	DE,(.INL_BUFST)	; DE = バッファ先頭
.INL_TRIMLP
	PUSH	IX
	POP	HL		; HL = IX（現在位置）
	OR	A
	SBC	HL,DE		; HL = IX - bufstart
	LD	A,H
	OR	L
	JR	Z,.INL_TRIMEND	; バッファ先頭に到達 → 終了
	DEC	IX
	LD	A,(IX+0)
	CP	$21
	JR	C,.INL_TRIMLP
	INC	IX
.INL_TRIMEND
	LD	(IX+0),$00	; 終端
	POP	IX
	RET

.INL_CURROW
	DB	0
.INL_WIDTH
	DB	0
.INL_BUFST
	DW	0

; VRAM文字コード → ASCII変換
; 入力/出力: A
VRAM2ASCII:
	CP	$9F
	JR	Z,.V2A_BSLASH
	CP	$87
	JR	Z,.V2A_LBRACE
	CP	$F0
	JR	Z,.V2A_PIPE
	CP	$9E
	JR	Z,.V2A_TILDE
	RET
.V2A_BSLASH
	LD	A,$5C		; '\'
	RET
.V2A_LBRACE
	LD	A,$7B		; '{'
	RET
.V2A_PIPE
	LD	A,$7C		; '|'
	RET
.V2A_TILDE
	LD	A,$7E		; '~'
	RET

; インサートモード処理
; PRINTの前に呼び出す。挿入モードならVRAMを右シフトする。
; 入力: A = 文字
; 出力: A = 文字（そのまま）
GTINS_LOCAL:
	CP	$20
	RET	C		; 制御コードはスルー
	PUSH	HL
	LD	HL,INSFLG
	BIT	0,(HL)		; INSFLG bit0テスト（$FF→set, $00→reset）
	POP	HL
	RET	Z		; 上書きモード（Aは保持される）

	; --- 挿入モード ---
	PUSH	AF
	PUSH	BC
	PUSH	DE
	PUSH	HL

	; カーソルのVRAMオフセット算出
	LD	A,(CSRNY)
	LD	H,A
	LD	A,(XYADR)
	LD	L,A
	CALL	LOC1_LOCAL
	PUSH	HL		; [SP] = カーソルオフセット

	; 論理行末尾のVRAMオフセット算出
	CALL	GETLNL_LOCAL	; A = 末尾+1行番号
	LD	H,A
	LD	L,0
	CALL	LOC1_LOCAL	; HL = 末尾+1行先頭
	DEC	HL		; HL = 論理行最終バイト

	; 末尾文字チェック
	CALL	GETCHR_LOCAL	; A = 末尾文字
	CP	$20		; スペースか？
	JR	NZ,.GTINS_EXPAND

	; --- スペース → 右シフトのみ ---
	POP	DE		; DE = カーソルオフセット
	; HL = 末尾ofs
	CALL	VSHIFT_RIGHT
	; カーソル位置にスペース書き込み
	EX	DE,HL		; HL = カーソルオフセット
	LD	A,$20
	CALL	PUTCHR_LOCAL

	POP	HL
	POP	DE
	POP	BC
	POP	AF
	RET

.GTINS_EXPAND
	; --- 行拡張が必要 ---
	; A = 末尾文字（非スペース）を保存
	LD	(.GTINS_LASTCH),A

	; 論理行末尾+1行番号
	CALL	GETLNL_LOCAL	; A = 末尾+1行番号
	LD	(.GTINS_NXTROW),A

	CP	LINE
	JR	C,.GTINS_NOSC

	; 画面最下行 → スクロール必要
	; TODO: LSX-Dodgersとのスクロール同期が必要
	; 今回は行拡張を断念（ベル音）
	POP	HL		; カーソルofs捨て
	POP	HL
	POP	DE
	POP	BC
	POP	AF
	PUSH	AF
	LD	A,7		; BELL
	CALL	PRINT
	POP	AF
	RET

.GTINS_NOSC
	; LINTTB挿入: 新行位置以降を1バイト下にシフト
	LD	A,LINE-1
	LD	B,A
	LD	A,(.GTINS_NXTROW)
	SUB	B		; A = NXTROW - (LINE-1)
	NEG			; A = (LINE-1) - NXTROW = シフトバイト数
	JR	Z,.GTINS_NOLSH
	LD	C,A
	LD	B,0		; BC = シフトバイト数
	LD	HL,LINTTB+LINE-2
	LD	DE,LINTTB+LINE-1
	LDDR
.GTINS_NOLSH
	; 新行位置に$FF（前行の続き）を書き込み
	LD	A,(.GTINS_NXTROW)
	LD	E,A
	LD	D,0
	LD	HL,LINTTB
	ADD	HL,DE
	LD	(HL),$FF

	; VRAM挿入: 新行位置以降のVRAMをWIDTH分下にシフト
	LD	A,(.GTINS_NXTROW)
	LD	H,A
	LD	L,0
	CALL	LOC1_LOCAL	; HL = 新行先頭VRAMオフセット
	; 転送元 = HL
	; 転送先 = HL + WIDTH
	; バイト数 = (画面末尾) - 転送先
	LD	A,(AT_WIDTH)
	LD	C,A
	LD	B,0
	PUSH	HL		; 新行先頭ofs
	LD	D,H
	LD	E,L
	ADD	HL,BC		; HL = 新行先頭 + WIDTH = 転送先
	EX	DE,HL		; DE = 転送先, HL = 新行先頭(転送元)
	; バイト数: 画面末尾ofs - 転送先ofs
	PUSH	HL
	PUSH	DE
	LD	A,LINE
	LD	H,A
	LD	L,0
	CALL	LOC1_LOCAL	; HL = 画面末尾+1
	POP	DE		; DE = 転送先
	OR	A
	SBC	HL,DE		; HL = 転送バイト数
	LD	B,H
	LD	C,L
	POP	HL		; HL = 転送元
	LD	A,B
	OR	C
	JR	Z,.GTINS_NOVSH
	CALL	VBTRNS_LOCAL
.GTINS_NOVSH

	; 新行をスペースで埋める
	POP	HL		; HL = 新行先頭ofs
	PUSH	HL
	LD	A,(AT_WIDTH)
	LD	C,A
	LD	B,0
	LD	A,$20
	CALL	FILVRM_LOCAL

	; 末尾文字を新行先頭に転記
	POP	HL		; HL = 新行先頭ofs
	LD	A,(.GTINS_LASTCH)
	CALL	PUTCHR_LOCAL

	; カーソル位置〜元の末尾を右シフト
	POP	DE		; DE = カーソルofsを取得
	PUSH	DE
	; 元の末尾 = 新行先頭 - 1
	LD	A,(.GTINS_NXTROW)
	LD	H,A
	LD	L,0
	CALL	LOC1_LOCAL
	DEC	HL		; HL = 元の末尾ofs
	CALL	VSHIFT_RIGHT

	; カーソル位置にスペース
	POP	HL		; HL = カーソルofs
	LD	A,$20
	CALL	PUTCHR_LOCAL

	POP	HL
	POP	DE
	POP	BC
	POP	AF
	RET

.GTINS_NXTROW
	DB	0
.GTINS_LASTCH
	DB	0

