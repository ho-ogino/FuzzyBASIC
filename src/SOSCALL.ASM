; 実際にWIDTHを切り替える場合はこれを生かす……が、基本的には80x25のみ対応なのであまり意味はない
#IF exists IOCSWIDTH
IOCS_WIDCH:
	PUSH	BC
	PUSH	DE
	PUSH	HL

	; OS作業領域からWK1FD0を読んでスキャン周波数を判定
	PUSH	AF
	LD	A,(WBOOTBK+1)	; OSページ高位バイト
	LD	H,A
	LD	L,$BF		; WK1FD0オフセット
	LD	C,(HL)
	POP	AF

	CP	41
	JR	C,WIDTH40
	BIT	0,C
	LD	HL,_C8025L
	JR	Z,SETCRTC
	LD	HL,_C8025H
	JR	SETCRTC
WIDTH40:
	BIT	0,C
	LD	HL,_C4025L
	JR	Z,SETCRTC
	LD	HL,_C4025H
SETCRTC:
	LD	DE,_CRTCD
	LD	BC,16
	LDIR

	; OS作業領域(CRTCD〜WK1FD0)にもコピーして
	; LSX-Dodgersの_WIDTH/_PAGE_MINUS/_WIDTH_MINUS等を更新
	LD	HL,_CRTCD
	LD	A,(WBOOTBK+1)	; OSページ高位バイト
	LD	D,A
	LD	E,$B0		; CRTCD offset
	LD	BC,16
	LDIR
	; _HEIGHTも25に設定
